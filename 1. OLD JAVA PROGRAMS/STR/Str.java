package STR;

public class Str {
    public static void main(String[] args) {
        //There are 2 ways of creating string
        //1.String Literal
        Str x = "Dinesh";
        //2.String Keyword
        Str demoString = new Str("Dinesh");
        //Printing the string literal
        System.out.println(x);
        //printing the string keyword
        System.out.println(demoString);
    }
}
