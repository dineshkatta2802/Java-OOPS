package TEST_PROGRAMS;
public class DataTypes {
    
    public static void main(String[]args){
        boolean bl=true;//true or false:1bit
        byte b=126;//1byte:-128 to 127
        short sh=12345;//2bytes:-32768 to 32767
        int x=123456789;//4bytes:-2^31 to 2^31-1
        long l=1234567890L;//8bytes:-2^63 to 2^63 -1
        float f=5.67f;;//4bytes:-2^31 to 2^31-1
        double d=5.67891011;//8bytes:-2^63 to 2^63 -1
    }
}
