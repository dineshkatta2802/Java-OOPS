package Wrapperclass;

public class Autoboxing {
    public static void main(String[] args) {
        //lets illustarte a simple example of Auto-Boxing
        int x = 5;
        double y = 10.23;
        char z = 'D';

        Integer a = x;
        Double b = y;
        Character c = z;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }

}
